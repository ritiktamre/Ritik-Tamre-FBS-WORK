////FINDING AREA OF PERAMETER RECTANGLE OR CIRCLE

#include<stdio.h>
int main()
{
	int l=10,w=20;
	int rectangle=l*w;
	int perimeter=2*(l+w);
	printf("AREA OF RECTANGLE=%d",rectangle);
	printf("\nPERIMETER OF RECTANGLE=%d",perimeter);
	
	
	int pi=10,dia=2;
	int circle=pi*dia;
	int perimeter1=2*(pi+dia);
	printf("\nAREA OF CIRCLE=%d",circle);
	printf("\nPERIMETER OF CIRCLR=%d",perimeter1);
	
}
